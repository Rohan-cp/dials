class Article {
  constructor(id, author, category, date, description, link, photo, title) {
    this.id = id;
    this.author = author;
    this.category = category;
    this.date = date;
    this.description = description;
    this.link = link;
    this.photo = photo;
    this.title = title;
  }
}

export default Article;
